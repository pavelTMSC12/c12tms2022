public class P3 {
    public static void main(String[] args) {
        //Арифметические операции
        //++ (инкремент) и -- (декремент)
        int bb = 1;
        System.out.println(bb);//
        System.out.println(++bb);//
        System.out.println(bb++);//
        System.out.println(bb);//

        int b = 12;
        int kk = 1 + ++b;
        System.out.println(kk);
        System.out.println(b);


        System.out.println(7 / 4);//1
        System.out.println(7 % 4);//3

    }
}
