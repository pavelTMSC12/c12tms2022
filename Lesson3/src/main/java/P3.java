public class P3 {
    public static void main(String[] args) {
        //Арифметические операции
        //++ (инкремент) и -- (декремент)
        //a + b
        int bb = 1;
        System.out.println(bb);//
        System.out.println(++bb);//
        System.out.println(bb++);//
        System.out.println(bb);//

        int counter = 0;

//        counter = counter + 1;
        counter++;

        int b = 12;
        int kk = 1 + ++b;
        System.out.println(kk);//14
        System.out.println(b);//13

        //BigDecimal
        double ll = 7;
        System.out.println(ll / 4);//1
        System.out.println(7 % 4);//3

    }
}
